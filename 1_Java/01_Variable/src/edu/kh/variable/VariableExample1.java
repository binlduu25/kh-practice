package edu.kh.variable;

public class VariableExample1 { // class : 코드를 작성하는 곳
	public static void main(String[] args) {
	
		//변수 사용 예시
		
		double pi = 3.141592653589793; //double : 8byte 실수 기본형
		int r = 15; //반지름(radius)
		int h = 25; //높이(height)
		
		System.out.println("원의 둘레는 "+"'"+(2*pi*r)+"'"+" 입니다."); //원의 둘레
		System.out.println("원의 넓이는 "+"'"+(r*r*pi)+"'"+" 입니다."); //원의 넓이
		System.out.println("원기둥의 부피는 "+"'"+(pi*r*r*h)+"'"+" 입니다."); //원기둥의 부피
		System.out.println("구의 겉넓이는 "+"'"+(4*pi*r*r)+"'"+" 입니다."); //구의 겉넓이
				
		/* 변수(Variable)
		 	* JAVA의 기본 자료형 (알고 있어야 함)
		  		* boolean : 논리형 변수 선언
		  		* char : 문자형 변수 선언
		  		* String : 문자열 변수 선언
		  		* byte : 정수형 변수 선언, 1byte
		  		* short : 정수형 변수 선언, 2byte
		  		* int : 정수형 변수 선언, 4byte
		  		* long : 정수형 변수 선언, 8byte
		  		* float : 실수형 변수 선언, 4byte
		  		* double : 실수형 변수 선언, 8byte
		 *
		 * 
		 */
		
				
		
	}
}
